function seedOf(bookId: string): number {
  let seed = 0
  for (const ch of bookId) seed = (seed * 31 + ch.charCodeAt(0)) % 997
  return seed
}

/** 읽는 중: 장르 + 진도(%)에 맞는 생각거리 — bookId 기반 결정적, index로 순환 */
export function readingPrompt(bookId: string, pct: number, index: number, category: string): string {
  const pool = READING[genreBucket(category)]
  const stage = pct < 34 ? pool.early : pct < 67 ? pool.mid : pool.late
  return stage[(seedOf(bookId) + index) % stage.length]
}

